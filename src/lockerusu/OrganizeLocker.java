    private String convertToTimeFormat(int durasi) {
        int hours = durasi / 3600;
        int minutes = (durasi % 3600) / 60;
        int seconds = durasi % 60;
        return String.format("%02d:%02d:%02d", hours, minutes, seconds);
    }

