    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String idLoker = IDLokeryangInginDipinjam.getText();
        String nim = NIMpeminjam.getText();
        int jam = Integer.parseInt(FieldJam.getText());
        int menit = Integer.parseInt(FieldMenit.getText());
        int detik = Integer.parseInt(FieldDetik.getText());
        int durasiInSeconds = jam * 3600 + menit * 60 + detik; // Durasi dalam detik
        String durasi = convertToTimeFormat(durasiInSeconds); // Konversi ke format TIME

        try (Connection conn = DatabaseConnection.getConnection()) {
            // Periksa apakah loker sudah terpakai
            String checkSql = "SELECT status FROM locker WHERE id = ?";
            PreparedStatement checkStmt = conn.prepareStatement(checkSql);
            checkStmt.setString(1, idLoker);

            ResultSet rs = checkStmt.executeQuery();
            if (rs.next()) {
                boolean status = rs.getBoolean("status");
                if (status) {
                    JOptionPane.showMessageDialog(this, "Loker ini sudah dipinjam. Pilih loker lain.");
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "ID Loker tidak valid.");
                return;
            }

            // Hitung waktu selesai
            String calculateFinishTimeSql = "SELECT ADDTIME(NOW(), ?) AS waktu_selesai";
            PreparedStatement calculateStmt = conn.prepareStatement(calculateFinishTimeSql);
            calculateStmt.setString(1, durasi);

            ResultSet calculateRs = calculateStmt.executeQuery();
            String waktuSelesai = null;
            if (calculateRs.next()) {
                waktuSelesai = calculateRs.getString("waktu_selesai");
            }

            // Update tabel locker
            String updateLokerSql = "UPDATE locker SET status = ?, nim = ?, waktu_sisa = ? WHERE id = ?";
            PreparedStatement updateLokerStmt = conn.prepareStatement(updateLokerSql);
            updateLokerStmt.setBoolean(1, true); // true berarti loker terpakai
            updateLokerStmt.setString(2, nim);
            updateLokerStmt.setString(3, durasi); // Simpan durasi dalam format TIME
            updateLokerStmt.setString(4, idLoker);

            updateLokerStmt.executeUpdate();

            // Insert ke tabel locker_history
            String insertHistorySql = "INSERT INTO locker_history (NIM_user, locker_id, durasi, waktu_mulai, waktu_selesai) VALUES (?, ?, ?, NOW(), ?)";
            PreparedStatement insertHistoryStmt = conn.prepareStatement(insertHistorySql);
            insertHistoryStmt.setString(1, nim);
            insertHistoryStmt.setString(2, idLoker);
            insertHistoryStmt.setString(3, durasi);
            insertHistoryStmt.setString(4, waktuSelesai); // Masukkan waktu selesai

            insertHistoryStmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Loker berhasil dipinjam!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

